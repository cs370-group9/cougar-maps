package com.cougarmaps.model.graph.enums;

/**
 * Enum representing the different types of nodes in the graph.
 * Each node type corresponds to a specific location or feature in the building.
 */
public enum NodeType {
    CLASSROOM,  // Represents a classroom
    RESTROOM,   // Represents a restroom
    HALLWAY,    // Represents a hallway
    ELEVATOR,   // Represents an elevator
    DOOR,       // Represents a door
    UNKNOWN     // Represents an unknown type

}